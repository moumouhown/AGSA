         public void initAffichage()
        {
            
            try
        {
            Statement statement = this.connection.createStatement();
            ResultSet rs = statement.executeQuery("select * from ApprentiStagiaire where IDAS = '"+ID_APPRENTI+"'");
            
            while(rs.next()){
                this.jTextField1.setText(rs.getString(1));
                this.jTextField2.setText(rs.getString(2));
                this.jTextField3.setText(rs.getString(3));
                this.jTextField7.setText(rs.getString(4));
                this.jTextArea1.setText(rs.getString(5));
                this.jTextField8.setText(rs.getString(6));
                this.jFormattedTextField1.setText(rs.getString(7));
                this.jTextField5.setText(rs.getString(8));
                this.jTextField9.setText(rs.getString(9));
                this.jTextField10.setText(rs.getString(10));
            }
        
            
        }
        catch(Exception ex)
         {
           ex.printStackTrace();
         }
              
        }
         
         
         
       public void  ModifierApprenti()
      {
            String id_apprenti = "'"+this.jTextField1.getText()+"'";
            String nom = "'"+ this.jTextField2.getText()+"'";
            String prenom ="'" + this.jTextField3.getText()+"'";
            String date_naiss="'"+ this.jFormattedTextField1.getText()+"'";
            String lieu_naiss="'"+ this.jTextField5.getText()+"'";
            String num_tel=this.jTextField7.getText();
            String email="'"+ this.jTextField8.getText()+"'";
            String adresse= "'"+ this.jTextArea1.getText()+"'";
            String niveau_scolaire  = "'"+ this.jTextField9.getText()+"'";
            String num_ccp= this.jTextField10.getText();
            
            
               try
        {
            Statement statement = this.connection.createStatement();
            ResultSet rs = statement.executeQuery("update ApprentiStagiaire set nom ="+nom+", prenom ="+prenom+","
                    + " NUMTEL="+num_tel+", ADRESSE="+adresse+", EMAIL="+email+" ,"
                    + " DATENAISS="+date_naiss+", LIEUNAISS="+lieu_naiss+","
                    + " NIVEAUSCOLAIRE="+niveau_scolaire+", NUMCCP="+num_ccp+" where IDAS = '"+ID_APPRENTI+"'");
        
              JOptionPane.showMessageDialog(null , "Apprenti avec l'ID: "+this.ID_APPRENTI+" a été bien modifié dans la base de données", "Information", JOptionPane.INFORMATION_MESSAGE);   
             this.gestionApprentisFram.initAffichageListeApprenti();
             this.dispose();
        }
        catch(Exception ex)
         {
           ex.printStackTrace();
         }
    
               this.gestionApprentisFram.initAffichageListeApprenti();
               this.dispose();
               
               
      }

    
    
