    public void initAffichage() {
        this.jTextField2.setText(NOM);
        this.jTextField3.setText(PRENOM);

        try {

            Statement statement = this.connection.createStatement();
            ResultSet rs = statement.executeQuery("select max(idStage)+1 from Stage");

            int nbStage = 1;
            try {

                while (rs.next()) {
                    nbStage = new Integer(rs.getString(1)).intValue();
                }

            } catch (Exception ex) {
                nbStage = 1;
            }

            this.jTextField1.setText("" + nbStage);

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        

    }
   
