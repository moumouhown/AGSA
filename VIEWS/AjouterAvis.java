    public void initAffichage() {

        this.jTextField1.setText(ID_APPRENTI);
        this.jTextField3.setText(NOM);
        this.jTextField4.setText(PRENOM);

        try {

            Statement statement = this.connection.createStatement();
            ResultSet rs = statement.executeQuery("select idVisite from VisiteMedicale where idAS='" + ID_APPRENTI + "'");

         
            if (rs.next()) {

                ID_VISITE = rs.getString(1);
             }
             this.jTextField2.setText(ID_VISITE);
           

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

