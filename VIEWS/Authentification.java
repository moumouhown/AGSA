        public void checkUser()
    {
        if(this.jTextField1.getText().equals("") || this.jPasswordField1.getText().equals(""))
        {
           this.jLabel4.setText("Les deux champs (ID Utilisateur et mot de passe) sont obligatoire");
           this.jLabel4.setVisible(true);
        }
        else
        {
            String userID = this.jTextField1.getText();
            String motpasse = this.jPasswordField1.getText();
            try
            {
                Statement statement = this.connection.createStatement();
                ResultSet rs = statement.executeQuery("select * from Utilisateur where idUtilisateur ='"+userID+"' and motDePasse='"+motpasse+"'");
                String userRes ="";
                String motpasseRes ="";
                String typeRes ="";
                while(rs.next())
                {
                    userRes = rs.getString(1);
                    motpasseRes = rs.getString(2);
                    typeRes = rs.getString(4);
                }
                if(userRes.equals(""))
                {
                    this.jLabel4.setText("ID Utilisateur ou Mot de passe érronée !");
                    this.jLabel4.setVisible(true);
                }
                else
                {
                    Utilisateur u = new Utilisateur(userRes, motpasseRes, typeRes);
                    mainFram mfram = new mainFram(connection, u);
                    this.dispose();
                }
                
                
                
                
                
                
            }
            catch(Exception ex)
            {
                ex.printStackTrace();
            }
        }    
    }

     
